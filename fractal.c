#include "fractol.h"

void	draw_fractal(t_fractal *f)
{
	int	y;

	y = 0;
	while (y < HEIGHT)
	{
		draw_line(f, y);
		y++;
	}
	mlx_put_image_to_window(f->mlx, f->win, f->img, 0, 0);
}
