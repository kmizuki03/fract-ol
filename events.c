#include "fractol.h"

int	handle_mouse(int button, int x, int y, t_fractal *f)
{
	if (button == ZOOM_IN || button == ZOOM_OUT)
	{
		handle_zoom(f, button, x, y);
		draw_fractal(f);
	}
	return (0);
}

double	ft_atof_integer(const char **str)
{
	double	result;

	result = 0.0;
	while (**str >= '0' && **str <= '9')
	{
		result = result * 10.0 + (**str - '0');
		(*str)++;
	}
	return (result);
}
